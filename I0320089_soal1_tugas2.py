print("Konversi Suhu Celcius ke Fahrenheit")
celcius = float(input("Suhu dalam celcius: "))
fahrenheit = ((9/5) * celcius) + 32
print(celcius, "celcius =", fahrenheit, "fahrenheit")

print("----------------------------------")

print("Konversi Suhu Reamur ke Kelvin")
reamur = float(input("Suhu dalam reamur: "))
kelvin = ((5/4) * reamur) + 273
print(reamur, "reamur =", kelvin, "kelvin")






